    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
        // TODO add your handling code here:
         String cedulaAux, nombreAux, apellidoAux, generoAux, direccionAux, edadAux, correoAux, numUnicoAux;
        
        cedulaAux = this.txtCedula.getText();
        nombreAux = this.txtNombre.getText();
        apellidoAux = this.txtApellido.getText();
        generoAux = cmbGenero.getSelectedItem().toString();
        direccionAux =this.txtDireccion.getText();
        edadAux = cmbEdad.getSelectedItem().toString();
        correoAux = this.txtCorreo.getText();
        numUnicoAux = this.txtNumUnico.getText();
        
        //Persona personaAux = new Persona(cedulaAux, nombreAux, apellidoAux, generoAux, direccionAux, Integer.parseInt(edadAux));
        
        Estudiante estudianteAux = new Estudiante(cedulaAux, nombreAux, apellidoAux, generoAux, direccionAux,Integer.parseInt(edadAux),Integer.parseInt(numUnicoAux), correoAux );
        
        //this.txtMostrarPersona.setText(personaAux.toString());
        this.txtMostrarPersona.setText(estudianteAux.toString());
        
    }//GEN-LAST:event_btnMostrarActionPerformed

